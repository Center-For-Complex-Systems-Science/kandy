# Define the symbolic function heads that KAN used
ID  = sp.Function('id')
NEG = sp.Function('-x^2')

# Collapse id(⋅) -> ⋅ and -x^2(z) -> -(z)**2
def collapse(e):
    e = e.replace(lambda z: isinstance(z, sp.FunctionClass), lambda z: z)  # no-op safety
    e = e.replace(lambda z: z.func == ID,  lambda z: z.args[0])
    e = e.replace(lambda z: z.func == NEG, lambda z: -(z.args[0])**2)
    return sp.expand(e)

y1 = collapse(y1)
y2 = collapse(y2)

# Round numeric constants to *three decimal places*
def round_numbers(expr, places=3):
    return expr.xreplace({a: round(float(a), places) for a in expr.atoms(sp.Number)})

