@torch.no_grad()
def rollout_model(model, x0, T):
    """Autoregressive rollout: x_{t+1} = model(x_t)"""
    model.eval()
    x = x0.unsqueeze(0) if x0.ndim == 1 else x0.clone()
    traj = [x.squeeze(0)]
    for _ in range(T - 1):
        x = model(x)
        traj.append(x.squeeze(0))
    return torch.stack(traj, dim=0)


def save_kan_architecture_figure(
    model,
    dataset,
    beta,
    png_path="kan_architecture.png",
    pdf_path="kan_architecture.pdf",
):
    """
    Runs a forward pass to populate activations (save_act=True),
    calls model.plot(...), and saves the resulting figure as PNG and PDF.
    """
    model.save_act = True
    with torch.no_grad():
        _ = model(dataset["test_input"])

