# Generate Hénon map data
def henon_step(state, a=1.4, b=0.3):
    x, y = state
    x_next = 1 - a * x**2 + y
    y_next = b * x
    return np.array([x_next, y_next], dtype=np.float32)

def generate_henon(n_total=12000, burn_in=1000, x0=0.1, y0=0.0, a=1.4, b=0.3):
    xy = np.zeros((n_total + burn_in, 2), dtype=np.float32)
    xy[0] = [x0, y0]
    for i in range(n_total + burn_in - 1):
        xy[i+1] = henon_step(xy[i], a=a, b=b)
    return xy[burn_in:]

