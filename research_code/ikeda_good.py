def build_ikeda_features_np(x, y):
    one = np.ones_like(x)
    r2 = x*x + y*y
    q  = 1.0 / (1.0 + r2)
    t  = 0.4 - 6.0 * q
    ct = np.cos(t)
    st = np.sin(t)

    feats = [
        #one,
        #x, y,
        #ct, st,
        u*x*ct, u*y*ct, u*x*st, u*y*st,
    ]
    names = [
        # "1",
        #"x","y",
        #"r2","q",
        #"x^2","y^2","x*y",
        #"t",
        #"cos(t)","sin(t)",
        "x*cos(t)","y*cos(t)","x*sin(t)","y*sin(t)",
        #"x*cos(t)-y*sin(t)", "x*sin(t)+y*cos(t)"
    ]
    Theta = np.stack(feats, axis=-1).astype(np.float32)
    return Theta, names

