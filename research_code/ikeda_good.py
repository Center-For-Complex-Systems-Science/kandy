# Ikeda (Ikeda-1) map data (discrete-time)
# x_{n+1} = 1 + u*(x_n cos t_n - y_n sin t_n)
# y_{n+1} =     u*(x_n sin t_n + y_n cos t_n)
# t_n = 0.4 - 6/(1 + x_n^2 + y_n^2)
