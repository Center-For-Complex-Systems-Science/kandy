class IkedaStructured(nn.Module):
    def __init__(self, use_residual_kan=False, residual_width=8, grid=5, k=3, seed=0):
        super().__init__()
        # Learnable parameters for t = a - b/(1+r^2) and shared u
        self.a = nn.Parameter(torch.tensor(0.0))  # init near 0
        self.b = nn.Parameter(torch.tensor(1.0))  # init near 1
        self.u = nn.Parameter(torch.tensor(0.5))  # init near 0.5

        self.use_residual_kan = use_residual_kan
        if use_residual_kan:
            # residual: (phi1,phi2) -> (dx,dy) added to u*[phi1,phi2]
            self.res = KAN(width=[2, residual_width, 2], grid=grid, k=k, base_fun='zero', seed=seed)
        else:
            self.res = None

    def phi(self, state_xy):
        x = state_xy[:, 0]
        y = state_xy[:, 1]
        r2 = x*x + y*y
        t  = self.a - self.b / (1.0 + r2)
        ct = torch.cos(t)
        st = torch.sin(t)
        phi1 = x*ct - y*st
        phi2 = x*st + y*ct
        return torch.stack([phi1, phi2], dim=1), t

    def forward(self, state_xy):
        # returns shifted targets: [x_{n+1}-1, y_{n+1}]
        phi, _t = self.phi(state_xy)
        y_shift = self.u * phi
        if self.res is not None:
            y_shift = y_shift + self.res(phi)
        return y_shift

    def step_state(self, state_xy):
        # build next (x,y) state from predicted shifted outputs
        y_shift = self.forward(state_xy)
        x_next = y_shift[:, 0] + 1.0
        y_next = y_shift[:, 1]
        return torch.stack([x_next, y_next], dim=1)

