# -----------------------
# Discrete rollout loss (iterate learned map)
# We roll in STATE space:
#   given state s_n, compute phi(s_n), predict y_shift = [x_{n+1}-1, y_{n+1}]
#   reconstruct predicted next state: x_{n+1}=y_shift[0]+1, y_{n+1}=y_shift[1]
# -----------------------
def map_step_from_state(state_xy):
    Phi = build_phi_torch(state_xy)       # (B,2)
    Phi_n = (Phi - X_mean) / X_std        # normalize
    y_shift = model(Phi_n)                # (B,2) = [x_{n+1}-1, y_{n+1}]
    x_next = y_shift[:, 0] + 1.0
    y_next = y_shift[:, 1]
    return torch.stack([x_next, y_next], dim=1)

def rollout_loss(seq_states, horizon=20, n_starts=512):
    T = seq_states.shape[0]
    if T <= horizon + 2:
        return torch.tensor(0.0, device=device)

    idx0 = torch.randint(0, T - horizon - 1, (n_starts,), device=device)
    s = seq_states[idx0]  # (B,2)

    loss = 0.0
    for k in range(1, horizon + 1):
        s = map_step_from_state(s)
        s_true = seq_states[idx0 + k]
        loss = loss + torch.mean((s - s_true) ** 2)
    return loss / horizon

