def ikeda_step(state, u=0.9):
    x, y = state
    t = 0.4 - 6.0 / (1.0 + x*x + y*y)
    x_next = 1.0 + u * (x * np.cos(t) - y * np.sin(t))
    y_next =        u * (x * np.sin(t) + y * np.cos(t))
    return np.array([x_next, y_next], dtype=np.float32)

def generate_ikeda(n_total=12000, burn_in=2000, x0=0.1, y0=0.1, u=0.9):
    traj = np.zeros((n_total + burn_in, 2), dtype=np.float32)
    traj[0] = np.array([x0, y0], dtype=np.float32)
    for i in range(n_total + burn_in - 1):
        traj[i + 1] = ikeda_step(traj[i], u=u)
    return traj[burn_in:]

# -----------------------
# Compressed physics features: phi1, phi2
# phi1 = x cos t - y sin t
# phi2 = x sin t + y cos t
# where t = 0.4 - 6/(1 + x^2 + y^2)
# -----------------------
def build_phi_np(x, y):
    r2 = x*x + y*y
    t  = 0.4 - 6.0 / (1.0 + r2)
    ct = np.cos(t)
    st = np.sin(t)
    phi1 = x*ct - y*st
    phi2 = x*st + y*ct
    return np.stack([phi1, phi2], axis=-1).astype(np.float32)

def build_phi_torch(state_xy):
    x = state_xy[:, 0]
    y = state_xy[:, 1]
    r2 = x*x + y*y
    t  = 0.4 - 6.0 / (1.0 + r2)
    ct = torch.cos(t)
    st = torch.sin(t)
    phi1 = x*ct - y*st
    phi2 = x*st + y*ct
    return torch.stack([phi1, phi2], dim=1)  # (B,2)

