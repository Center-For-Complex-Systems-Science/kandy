def rollout_loss(seq_states, horizon=10, n_starts=256):
    """
    seq_states: (T,2) torch, contiguous trajectory
    picks random start indices, rolls forward 'horizon' steps, compares to truth
    """
    T = seq_states.shape[0]
    if T <= horizon + 1:
        return torch.tensor(0.0, device=device)

    # random starts in [0, T-horizon-1]
    idx0 = torch.randint(0, T - horizon - 1, (n_starts,), device=device)
    s = seq_states[idx0]  # (B,2)

    loss = 0.0
    for k in range(1, horizon + 1):
        s = map_fn_from_state(s)  # predict next
        s_true = seq_states[idx0 + k]
        loss = loss + torch.mean((s - s_true) ** 2)
    return loss / horizon

