# ============================================================
# Holling Type II predator-prey ODE (Rosenzweig–MacArthur)
# dN/dt = r N (1 - N/K) - (a N P)/(1 + a h N)
# dP/dt = e (a N P)/(1 + a h N) - m P
# ============================================================

def holling_rhs_np(state, r, K, a, h, e, m):
    N, P = state
    denom = 1.0 + a*h*N
    pred  = (a*N*P) / denom
    dN = r*N*(1.0 - N/K) - pred
    dP = e*pred - m*P
    return np.array([dN, dP], dtype=np.float32)

def rk4_step_np(state, dt, r, K, a, h, e, m):
    k1 = holling_rhs_np(state, r,K,a,h,e,m)
    k2 = holling_rhs_np(state + 0.5*dt*k1, r,K,a,h,e,m)
    k3 = holling_rhs_np(state + 0.5*dt*k2, r,K,a,h,e,m)
    k4 = holling_rhs_np(state + dt*k3, r,K,a,h,e,m)
    return (state + (dt/6.0)*(k1 + 2*k2 + 2*k3 + k4)).astype(np.float32)

def generate_holling_traj_np(
    n_total=3000,
    burn_in=200,
    dt=0.02,
    x0=(10.0, 5.0),
    params=(1.0, 50.0, 1.2, 0.1, 0.6, 0.4),
    noise_std=0.0
):
    r, K, a, h, e, m = params
    T = n_total + burn_in
    traj = np.zeros((T, 2), dtype=np.float32)
    traj[0] = np.array(x0, dtype=np.float32)

    for i in range(T - 1):
        traj[i+1] = rk4_step_np(traj[i], dt, r,K,a,h,e,m)
        if noise_std > 0:
            traj[i+1] += rng.normal(0.0, noise_std, size=(2,)).astype(np.float32)
        traj[i+1] = np.maximum(traj[i+1], 0.0)
    return traj[burn_in:]

# ============================================================
# Physics-informed feature library (NumPy)
# ============================================================

def build_holling_features_np(N, P, *, r, K, a, h, e, m):
    one = np.ones_like(N)

    denom  = 1.0 + a*h*N
    invden = 1.0 / denom
    fN     = N * invden
    pred   = (a*N*P) * invden
    logistic = r*N*(1.0 - N/K)
    deathP   = m*P
    gainP    = e*pred

    feats = [
        one, N, P,
        N*N, P*P, N*P,
        denom, invden, fN,
        logistic, pred, gainP, deathP,
        # optional aligned combos
        r*N, (N/K), r*N*(N/K),
        a*N, a*P, (a*N*P),
        (a*h*N), (a*h*N)*P,
    ]

    names = [
        "1","N","P",
        "N^2","P^2","N*P",
        "denom","invden","fN",
        "logistic","pred","gainP","deathP",
        "r*N","N/K","r*N*(N/K)",
        "a*N","a*P","a*N*P",
        "a*h*N","(a*h*N)*P",
    ]
    Theta = np.stack(feats, axis=-1).astype(np.float32)
    return Theta, names

