# Symbolic extraction with PHYSICS-FEATURE complexity down-weighting
#
# Key idea:
#   - physics-informed features get LOWER cost library (PHYS_SYMBOLIC_LIB)
#   - generic features get HIGHER cost library (GEN_SYMBOLIC_LIB)
# so the solver prefers physics features when fit is comparable.
