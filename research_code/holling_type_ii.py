def build_holling_features_torch(state_NP, *, r, K, a, h, e, m):
    N = state_NP[:, 0]
    P = state_NP[:, 1]
    one = torch.ones_like(N)

    denom  = 1.0 + a*h*N
    invden = 1.0 / denom
    fN     = N * invden
    pred   = (a*N*P) * invden
    logistic = r*N*(1.0 - N/K)
    deathP   = m*P
    gainP    = e*pred

