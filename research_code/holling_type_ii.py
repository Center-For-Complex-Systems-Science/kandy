# Physics-informed feature library (Torch for training/rollouts)
# ============================================================

def build_holling_features_torch(state_NP, *, r, K, a, h, e, m):
    # state_NP: (B,2)
    N = state_NP[:, 0]
    P = state_NP[:, 1]
    one = torch.ones_like(N)

    denom  = 1.0 + a*h*N
    invden = 1.0 / denom
    fN     = N * invden
    pred   = (a*N*P) * invden
    logistic = r*N*(1.0 - N/K)
    deathP   = m*P
    gainP    = e*pred

    feats = torch.stack([
        one, N, P,
        N*N, P*P, N*P,
        denom, invden, fN,
        logistic, pred, gainP, deathP,
        r*N, (N/K), r*N*(N/K),
        a*N, a*P, (a*N*P),
        (a*h*N), (a*h*N)*P,
    ], dim=1)
    return feats  # (B,F)

