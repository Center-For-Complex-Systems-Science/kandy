# ============================================================
# Holling Type II predator-prey ODE (Rosenzweig–MacArthur form)
#
# dN/dt = r N (1 - N/K) - (a N P) / (1 + a h N)
# dP/dt = e (a N P) / (1 + a h N) - m P
#
# N: prey, P: predator
# ============================================================

def holling_rhs(state, r, K, a, h, e, m):
    N, P = state
    # functional response denominator
    denom = 1.0 + a*h*N
    pred  = (a*N*P) / denom
    dN = r*N*(1.0 - N/K) - pred
    dP = e*pred - m*P
    return np.array([dN, dP], dtype=np.float32)

def rk4_step(state, dt, r, K, a, h, e, m):
    k1 = holling_rhs(state, r,K,a,h,e,m)
    k2 = holling_rhs(state + 0.5*dt*k1, r,K,a,h,e,m)
    k3 = holling_rhs(state + 0.5*dt*k2, r,K,a,h,e,m)
    k4 = holling_rhs(state + dt*k3, r,K,a,h,e,m)
    return (state + (dt/6.0)*(k1 + 2*k2 + 2*k3 + k4)).astype(np.float32)

def generate_holling(
    n_total=20000,
    burn_in=2000,
    dt=0.01,
    x0=(10.0, 5.0),
    params=(1.0, 50.0, 1.2, 0.1, 0.6, 0.4),
    noise_std=0.0
):
    """
    Returns a single long discrete-time series:
      series[k] ~ state at time k*dt  (after burn-in)
    """
    r, K, a, h, e, m = params
    T = n_total + burn_in
    traj = np.zeros((T, 2), dtype=np.float32)
    traj[0] = np.array(x0, dtype=np.float32)

    for i in range(T - 1):
        traj[i+1] = rk4_step(traj[i], dt, r,K,a,h,e,m)

        # optional measurement noise (keep small if you use it)
        if noise_std > 0:
            traj[i+1] += rng.normal(0.0, noise_std, size=(2,)).astype(np.float32)

        # enforce nonnegativity (common in ecology sims)
        traj[i+1] = np.maximum(traj[i+1], 0.0)

    return traj[burn_in:]

