def rollout_loss_rhs(traj, horizon=50, n_starts=256, dt=0.02):
    """
    traj: (T,2) torch trajectory from simulator
    Roll forward using predicted RHS + Euler:
      s_{k+1} = s_k + dt * f_theta(s_k)
    Compare to true states along the trajectory.
    """
    T = traj.shape[0]
    if T <= horizon + 1:
        return torch.tensor(0.0, device=device)

    idx0 = torch.randint(0, T - horizon - 1, (n_starts,), device=device)
    s = traj[idx0]  # (B,2)
    loss = 0.0
    for k in range(1, horizon + 1):
        ds = rhs_fn_from_state(s)
        s  = s + dt * ds
        s  = torch.clamp(s, min=0.0)  # keep nonnegative
        s_true = traj[idx0 + k]
        loss = loss + torch.mean((s - s_true) ** 2)
    return loss / horizon

