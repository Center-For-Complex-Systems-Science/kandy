# Physics-informed feature library (NumPy for dataset build)
# ============================================================

def build_holling_features_np(N, P, *, r, K, a, h, e, m):
    """
    Wide library includes:
      - generic terms: 1, N, P, N^2, P^2, N*P, ...
      - physics terms (computed using known params):
          logistic = r*N*(1 - N/K)
          denom    = 1 + a*h*N
          fN       = N/denom
          pred     = (a*N*P)/denom
          e*pred,  m*P
      - combos that align with RHS structure
    """
    one = np.ones_like(N)
    denom  = 1.0 + a*h*N
    invden = 1.0 / denom
    fN     = N * invden                # N/(1+a h N)
    pred   = (a*N*P) * invden          # a N P/(1+a h N)
    logistic = r*N*(1.0 - N/K)         # r N (1 - N/K)
    deathP   = m*P
    gainP    = e*pred

    feats = [
        one, N, P,
        N*N, P*P, N*P,
        denom, invden, fN,
        logistic, pred, gainP, deathP,
        # extra aligned combos (often helps symbolic extraction)
        r*N, (N/K), r*N*(N/K),
        a*N, a*P, (a*N*P),
        (a*h*N), (a*h*N)*P,
    ]

    names = [
        "1","N","P",
        "N^2","P^2","N*P",
        "denom","invden","fN",
        "logistic","pred","gainP","deathP",
        "r*N","N/K","r*N*(N/K)",
        "a*N","a*P","a*N*P",
        "a*h*N","(a*h*N)*P",
    ]

    Theta = np.stack(feats, axis=-1).astype(np.float32)
    return Theta, names

