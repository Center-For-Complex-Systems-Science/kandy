import numpy as np


def wrap_angle(x):
    """Map angles to [0, 2π)."""
    return np.mod(x, 2.0 * np.pi)


def kuramoto_order_parameter(phi):
    """Return complex order parameter Z = r e^{iψ} and r."""
    Z = np.mean(np.exp(1j * phi))
    return Z, np.abs(Z)



def simulate_adaptive_kuramoto(
    N: int = 100,
    T: float = 200.0,
    dt: float = 0.01,
    alpha: float = 0.3 * np.pi,
    beta: float = 0.23 * np.pi,
    eps: float = 0.01,
    omega=0.0,
    phi0=None,
    kappa0=None,
    keep_diag_zero: bool = True,
    method: str = "euler",   # "euler" or "rk4"
    store_every: int = 1,
    seed: int | None = 0,
):
    """
    Simulate the adaptive Kuramoto–Sakaguchi model (28)–(29):
        φ̇_i = ω - (1/N) Σ_j κ_ij sin(φ_i - φ_j + α)
        κ̇_ij = -ε (κ_ij + sin(φ_i - φ_j + β))

    Parameters
    ----------
    omega : float or array-like shape (N,)
        Natural frequency ω (scalar) or ω_i (vector). Eq. (28) uses ω; vector is a common extension.
    phi0 : array-like shape (N,), optional
        Initial phases. If None, uniform random in [0, 2π).
    kappa0 : array-like shape (N,N), optional
        Initial coupling weights. If None, start at 0.
    method : "euler" or "rk4"
        Time stepping method.
    store_every : int
        Store every k-th step to reduce memory.
    """
    rng = np.random.default_rng(seed)

    # omega as vector
    if np.isscalar(omega):
        omega = np.full(N, float(omega))
    else:
        omega = np.asarray(omega, dtype=float)
        assert omega.shape == (N,)

    # initial conditions
    if phi0 is None:
        phi = rng.uniform(0.0, 2.0 * np.pi, size=N)
    else:
        phi = np.asarray(phi0, dtype=float).copy()
        assert phi.shape == (N,)
        phi = wrap_angle(phi)

    if kappa0 is None:
        kappa = np.zeros((N, N), dtype=float)
    else:
        kappa = np.asarray(kappa0, dtype=float).copy()
        assert kappa.shape == (N, N)

    if keep_diag_zero:
        np.fill_diagonal(kappa, 0.0)

    n_steps = int(np.round(T / dt))
    n_store = n_steps // store_every + 1

    t_hist = np.empty(n_store, dtype=float)
    phi_hist = np.empty((n_store, N), dtype=float)
    r_hist = np.empty(n_store, dtype=float)

    def rhs(phi, kappa):
        """
        Compute (dphi, dkappa) given current state.
        Vectorized pairwise differences Δ_ij = φ_i - φ_j.
        """
        # Δ_ij = φ_i - φ_j
        dphi_ij = phi[:, None] - phi[None, :]

        # phase dynamics
        interaction = kappa * np.sin(dphi_ij + alpha)      # κ_ij sin(φ_i - φ_j + α)
        dphi = omega - (1.0 / N) * np.sum(interaction, axis=1)

        # coupling adaptation
        dkappa = -eps * (kappa + np.sin(dphi_ij + beta))

        if keep_diag_zero:
            # enforce κ_ii = 0 and κ̇_ii = 0 (often used; Eq. (29) doesn't forbid self-coupling)
            np.fill_diagonal(dkappa, 0.0)

        return dphi, dkappa

    def step_euler(phi, kappa):
        dphi, dkappa = rhs(phi, kappa)
        phi_new = wrap_angle(phi + dt * dphi)
        kappa_new = kappa + dt * dkappa
        if keep_diag_zero:
            np.fill_diagonal(kappa_new, 0.0)
        return phi_new, kappa_new

    def step_rk4(phi, kappa):
        dphi1, dk1 = rhs(phi, kappa)
        p2 = wrap_angle(phi + 0.5 * dt * dphi1)
        k2 = kappa + 0.5 * dt * dk1

        dphi2, dk2 = rhs(p2, k2)
        p3 = wrap_angle(phi + 0.5 * dt * dphi2)
        k3 = kappa + 0.5 * dt * dk2

        dphi3, dk3 = rhs(p3, k3)
        p4 = wrap_angle(phi + dt * dphi3)
        k4 = kappa + dt * dk3

        dphi4, dk4 = rhs(p4, k4)

        phi_new = wrap_angle(phi + (dt / 6.0) * (dphi1 + 2*dphi2 + 2*dphi3 + dphi4))
        kappa_new = kappa + (dt / 6.0) * (dk1 + 2*dk2 + 2*dk3 + dk4)
        if keep_diag_zero:
            np.fill_diagonal(kappa_new, 0.0)
        return phi_new, kappa_new

    stepper = step_euler if method.lower() == "euler" else step_rk4

    # store initial
    idx = 0
    t_hist[idx] = 0.0
    phi_hist[idx] = phi
    _, r_hist[idx] = kuramoto_order_parameter(phi)

    for n in range(1, n_steps + 1):
        phi, kappa = stepper(phi, kappa)

        if (n % store_every) == 0:
            idx += 1
            t_hist[idx] = n * dt
            phi_hist[idx] = phi
            _, r_hist[idx] = kuramoto_order_parameter(phi)

    return {
        "t": t_hist,
        "phi": phi_hist,
        "r": r_hist,
        "kappa_final": kappa,
    }


