    diffs_off = dphi_ij[:, mask]                                      # (B,M)
    X_b = torch.cat([diffs_off, k_off], dim=1)                        # (B, 2M)

    # --- dphi from KAN ---
    dphi_b = kan_adaptive(X_b)                                        # (B, N)

    # --- dkappa from known adaptation law (off-diagonal only) ---
    # dkappa_ij = -eps * (kappa_ij + sin(phi_i - phi_j + beta))
    # note: here dphi_ij uses (i - j); your sim uses phi_i - phi_j, consistent.
    dkappa_b = -eps * (kappa_b + torch.sin(dphi_ij + beta))           # (B,N,N)
    dkappa_off = dkappa_b[:, mask]                                    # (B,M)

    return torch.cat([dphi_b, dkappa_off], dim=1)                     # (B, N+M)

