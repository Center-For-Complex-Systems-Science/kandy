TWOPI = 2 * np.pi

def wrap_pi(x):
    return (x + np.pi) % (2*np.pi) - np.pi

def wrap_pi_torch(x):
    return (x + np.pi) % (2*np.pi) - np.pi

def unwrap_for_plot(traj_wrapped):
    return np.unwrap(traj_wrapped, axis=0)

def rk4_step_vec(f, y, dt, **kwargs):
    k1 = f(y, **kwargs)
    k2 = f(y + 0.5*dt*k1, **kwargs)
    k3 = f(y + 0.5*dt*k2, **kwargs)
    k4 = f(y + dt*k3, **kwargs)
    return y + (dt/6.0)*(k1 + 2*k2 + 2*k3 + k4)

# -----------------------------
# True Kuramoto RHS (ground truth)
# -----------------------------
def kuramoto_rhs(theta, omega, K=1.0, A=None):
    """
    Full Kuramoto RHS:
        dθ_i/dt = ω_i + K * Σ_j A_ij sin(θ_j - θ_i)
    """
    N = theta.shape[0]
    if A is None:
        A = np.ones((N, N), dtype=np.float64) / N
        np.fill_diagonal(A, 0.0)
    diffs = wrap_pi(theta[None, :] - theta[:, None])  # (i,j) = θ_j - θ_i
    coupling = (A * np.sin(diffs)).sum(axis=1)
    return omega + K * coupling

# -----------------------------
# Derivative estimate from a wrapped trajectory
# -----------------------------
def estimate_dtheta_from_traj(traj_wrapped, dt, method="central"):
    # differentiate unwrapped phases to avoid wrap artifacts
    theta_unwrapped = np.unwrap(traj_wrapped, axis=0)
    if method == "forward":
        dtheta = (theta_unwrapped[1:] - theta_unwrapped[:-1]) / dt
        theta_mid = traj_wrapped[:-1]
    elif method == "central":
        dtheta = (theta_unwrapped[2:] - theta_unwrapped[:-2]) / (2.0 * dt)
        theta_mid = traj_wrapped[1:-1]
    else:
        raise ValueError("method must be 'forward' or 'central'")
    return theta_mid, dtheta

# -----------------------------
# FEATURE MAP (IMPORTANT CHANGE)
# -----------------------------
# Model inputs should be the phase difference in theta.
# Since base_fun=torch.sin, KAN will learn the sine of these diffs internally.

def build_features_diff_offdiag(theta_t, A, include_self=False, weight_mode="mul"):
    """
    NumPy features: flattened off-diagonal of raw diffs (θ_j - θ_i).

    """
    N = theta_t.shape[0]
    diffs = wrap_pi(theta_t[None, :] - theta_t[:, None])  # θ_j - θ_i  (i,j)
    if not include_self:
        np.fill_diagonal(diffs, 0.0)

    mask = ~np.eye(N, dtype=bool)
    diffs_off = diffs[mask]  # (N*(N-1),)

    return diffs_off


def build_features_diff_offdiag_torch(theta_b, A_torch, include_self=False):
    """
    Torch features: (B, N) -> (B, N*(N-1)) (or doubled if 'cat')
    diffs[i,j] = θ_j - θ_i, wrapped to [-pi, pi)
    """
    B, N = theta_b.shape
    diffs = wrap_pi_torch(theta_b[:, None, :] - theta_b[:, :, None])  # (B,N,N)

    if not include_self:
        eye = torch.eye(N, device=theta_b.device, dtype=torch.bool)
        diffs = diffs.masked_fill(eye.unsqueeze(0), 0.0)

    mask = ~torch.eye(N, device=theta_b.device, dtype=torch.bool)
    diffs_off = diffs[:, mask]  # (B, N*(N-1))

    return diffs_off


# -----------------------------
# Rollout helper (NumPy) for plotting / evaluation
# -----------------------------
def rollout_rhs(rhs_fn, theta0, T_roll, dt):
    steps_r = int(T_roll / dt)
    tr = np.zeros((steps_r + 1, theta0.shape[0]), dtype=np.float64)
    th = theta0.astype(np.float64).copy()
    tr[0] = th
    for k in range(steps_r):
        th = rk4_step_vec(lambda th_: rhs_fn(th_), th, dt)
        th = wrap_pi(th)
        tr[k + 1] = th
    return tr

