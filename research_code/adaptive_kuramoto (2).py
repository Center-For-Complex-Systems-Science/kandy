def rollout_with_kappa(dphi_fn, phi0, kappa0, T_roll, dt):
    """
    Integrate φ using dphi_fn; step κ analytically in parallel.
    """
    steps = int(T_roll / dt)
    phi_hist   = np.zeros((steps + 1, N_osc))
    kappa_hist = np.zeros((steps + 1, N_osc, N_osc))
    phi_hist[0]   = phi0
    kappa_hist[0] = kappa0

    for k in range(steps):
        phi_k   = phi_hist[k]
        kappa_k = kappa_hist[k]

        # φ step (RK4 using the KAN)
        def f_phi(ph): return dphi_fn(ph, kappa_k)
        k1 = f_phi(phi_k)
        k2 = f_phi(wrap_pi(phi_k + 0.5*dt*k1))
        k3 = f_phi(wrap_pi(phi_k + 0.5*dt*k2))
        k4 = f_phi(wrap_pi(phi_k + dt*k3))
        phi_new = wrap_pi(phi_k + (dt/6.0)*(k1 + 2*k2 + 2*k3 + k4))

        # κ step (Euler, analytical)
        dphi_ij = phi_k[:, None] - phi_k[None, :]
        dkappa  = -eps * (kappa_k + np.sin(dphi_ij + beta))
        np.fill_diagonal(dkappa, 0.0)
        kappa_new = kappa_k + dt * dkappa

        phi_hist[k+1]   = phi_new
        kappa_hist[k+1] = kappa_new

    return phi_hist, kappa_hist

