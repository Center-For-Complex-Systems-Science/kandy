def order_parameter(phi_traj: np.ndarray) -> np.ndarray:
    """
    Kuramoto order parameter r(t) for a phase trajectory.

    Parameters
    ----------
    phi_traj : np.ndarray, shape (T, N) or (N,)
        Phases in radians (can be wrapped or unwrapped; wrapping doesn't matter).

    Returns
    -------
    r : np.ndarray, shape (T,)  (or scalar if input is (N,))
        r(t) = | (1/N) * sum_j exp(i * phi_j(t)) |
    """
    phi_traj = np.asarray(phi_traj)
    if phi_traj.ndim == 1:  # (N,)
        return np.abs(np.mean(np.exp(1j * phi_traj)))
    elif phi_traj.ndim == 2:  # (T,N)
        return np.abs(np.mean(np.exp(1j * phi_traj), axis=1))
    else:
        raise ValueError(f"phi_traj must have shape (N,) or (T,N), got {phi_traj.shape}")

