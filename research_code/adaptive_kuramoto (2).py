def dynamics_fn(state_b: torch.Tensor) -> torch.Tensor:
    """
    state_b: (B, state_dim) where state_dim = N_osc + N_osc*(N_osc-1)
    returns: (B, state_dim) derivatives [dphi, dkappa_off]
    """
    B = state_b.shape[0]
    N = N_osc
    M = N * (N - 1)

    phi_b = state_b[:, :N]          # (B, N)
    k_off = state_b[:, N:N+M]       # (B, M)

    # --- reconstruct full kappa matrix (B,N,N) from off-diagonal vector ---
    kappa_b = torch.zeros((B, N, N), device=state_b.device, dtype=state_b.dtype)
    mask = torch.tensor(off_mask, device=state_b.device)
    kappa_b[:, mask] = k_off
    # diag remains 0 by construction

    # --- build KAN input features: [diffs_off, kappa_off] ---
